<dl>
  <dt>Formato da hora</dt>
  <dd>Os formatos suportados para os campos Hora de início e Hora de término são <span>HHMM</span> ou <span>HH:MM</span>. Por exemplo, <em>0930</em> ou <em>14:20</em>. Você também deve incluir o 0 inicial se estiver usando o formato HHMM.</dd>
	<dd>Todos os horários também devem ser inseridos no <span>24 horas</span> ou seja, entre as 00:00 e às 23:59.</dd>
</dl>
